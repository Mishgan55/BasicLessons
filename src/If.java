public class If {
    public static void main(String[] args) {
        int myInt=50;
        if (myInt>70){
            System.out.println("Верно");
        }else if (myInt<40){
            System.out.println("Не верно");

        }else {
            System.out.println("Ни одно из этих условий");
        }
    }
}
